package advanced_practice.practice06;

public class Hacim {

    public int hacimHesapla(int a){
        return a*a*a;
    }

    public int hacimHesapla(int a, int b){
        return a*a*b;
    }

    public int hacimHesapla(int a, int b, int c){
        return a*b*c;
    }
    public int hacimkureHesapla(int a){return 4*a*a*a;}

}