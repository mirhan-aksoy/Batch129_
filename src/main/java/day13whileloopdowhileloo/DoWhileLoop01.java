package day13whileloopdowhileloo;

public class DoWhileLoop01 {
    public static void main(String[] args) {

        //while-loop
        int i=1;
        while (i<1){
            System.out.println("sen bir while loopsun");
            i++;
        }
        //2 do-while-loop
        int k=1;
        do{
            System.out.println("sen bir Do-while-loop sun");
            k++;
        }while (k<1);



    }
}
